import Foundation

/**
 The `LogMessageType` defines a list of possible message types.
 */
enum LogMessageType {
    
    /// Error message.
    case error
    
    /// Warning message.
    case warning
    
    /// Debug message.
    case debug
    
    /// Info message.
    case info
}
