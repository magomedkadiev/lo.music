import Foundation

/// The Double extension.
extension Double {
    
    // MARK: - Properties
    
    /// The second.
    var second: TimeInterval {
        return self
    }
    
    /// The seconds.
    var seconds: TimeInterval {
        return self
    }
    
    /// The minute.
    var minute: TimeInterval {
        return self * 60
    }
    
    /// The minutes.
    var minutes: TimeInterval {
        return self * 60
    }
    
    /// The hour.
    var hour: TimeInterval {
        return self * 3600
    }
    
    /// The hours.
    var hours: TimeInterval {
        return self * 3600
    }
}
